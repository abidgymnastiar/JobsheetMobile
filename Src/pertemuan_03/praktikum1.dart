void main(List<String> args) {
  // Menerapkakan Control Flows ("if/Else")

  String test = "test2";
  if (test == "test1") {
    print("test1");
  } else if (test == "test2") {
    print("test2");
  } else {
    print("something else");
  }

  if (test == "test2") print("test2 again");

  String testAja = "true";
  if (testAja == "true") {
    print("Kebenaran");
  }
}
